package com.collection.interviewquestion.sortingArraywithNullvalues;

public class Student {
	private String name;
	private String age;
	private String classStudy;

	public Student(String name, String age, String classStudy) {
		super();
		this.name = name;
		this.age = age;
		this.classStudy = classStudy;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getClassStudy() {
		return classStudy;
	}

	public void setClassStudy(String classStudy) {
		this.classStudy = classStudy;
	}

}
